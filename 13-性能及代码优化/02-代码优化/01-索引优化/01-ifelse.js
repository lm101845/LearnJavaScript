function previewWeek(i) {
    if (i == 1) {
        return '星期一'
    } else if (i == 2) {
        return '星期二'
    } else if (i == 3) {
        return '星期三'
    } else if (i == 4) {
        return '星期四'
    } else if (i == 5) {
        return '星期五'
    } else if (i == 6) {
        return '星期六'
    } else if (i == 7) {
        return '星期日'
    } else {
        return ''
    }
}
